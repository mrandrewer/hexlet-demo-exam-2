class PartnerEditItem:
    def __init__(self,
                 id=None,
                 type=None,
                 partner_name=None,
                 director_name=None,
                 email=None,
                 phone=None,
                 address=None,
                 rating=None):
        self.id = id
        self.type = type
        self.partner_name = partner_name
        self.director_name = director_name
        self.email = email
        self.phone = phone
        self.address = address
        self.rating = rating
